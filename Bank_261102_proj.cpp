void history(string acc,string add)
{	
	string textline;
	ifstream his(acc+".txt");
	vector<string> n;
	while(getline(his,textline))
	{
		n.push_back(textline);
	}his.close();
	ofstream hist(acc+".txt");
	for(int i=0;i<n.size();i++){
		hist<<n[i]<<endl;
	}
	hist<<add<<endl;
	hist.close();
	
}
